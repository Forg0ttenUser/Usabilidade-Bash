# O "echo" é um  comando interno do shell utilizado, geralmente, para ecoar uma sequência de de caracteres.

echo Olá, mundo!

# O exemplo acima imprime a mensagem "Olá, mundo!" no fluxo de saída padrão.

echo -n "Não quebra uma nova linha"

# O exemplo acima exibe a mensagem "Não quebra uma nova linha" na saída padrão, sem quebrar uma linha.

# Nem sempre toda sequência de chars será exibida literalmente, pois o Bash possui uma tratamento semântico especial para certos chars.